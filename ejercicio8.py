from ast import main

def monstruo_del_lago_ness(cadena):
    import re
    patron = re.compile(r"tree fiddy|3\.50|three fifty") # patron a buscar
    coincidencia = re.search(patron, cadena) # busca la primera coincidencia
    if coincidencia:
        return True
    else:
        return False

